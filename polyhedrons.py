def plot_poly(v=v,e=e):
    f=pl.figure(figsize=(5,5))
    ax=f.add_subplot(111,projection='3d')
    for j in range(3): 
        for i in range(e.shape[0]):
            ed=[el-1 for el in e[i] if el!=0]
            ax.plot((j+1)*v[ed,0],
                    (j+1)*v[ed,1],
                    (j+1)*v[ed,2],
                    color=c[j])
    ax.set_axis_off(); pl.show(); pl.close()
plot_poly(v,e)

